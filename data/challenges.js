/**
 * Задания челленджей по осям (30 базовых × программы 7 и 30 дней).
 */
(function (global) {
  'use strict';

  var axes = [
    'authenticity',
    'presentation',
    'autonomy',
    'social',
    'reflection',
    'digital'
  ];

  var labels = {
    authenticity: 'Аутентичность',
    presentation: 'Самопрезентация',
    autonomy: 'Автономия',
    social: 'Социальная связь',
    reflection: 'Рефлексия',
    digital: 'Цифровая идентичность'
  };

  var tasks = [];
  var templates = [
    'Заметьте 3 момента, где вы выбираете образ вместо переживания.',
    'Напишите черновик поста только для себя, не публикуя.',
    'Ответьте одному человеку онлайн так, как ответили бы офлайн близкому.',
    'Сократите био до одной честной фразы (черновик).',
    'Сравните два своих поста: где больше «надо», где больше «хочу».',
    'Составьте список тем, о которых вы молчите публично — и почему.',
    'Прочитайте свои посты за неделю голосом вслух — отметьте диссонанс.',
    'Выберите одну маску из эксперимента и опишите, когда она полезна.',
    'Сделайте паузу 24ч без сторис — зафиксируйте чувства.',
    'Замените один «показной» пост на пост-процесс.',
    'Спросите у друга: узнал бы он вас по ленте?',
    'Запишите 5 слов, описывающих ваш «идеальный онлайн-образ».',
    'Запишите 5 слов про «реального себя» — найдите пересечения.',
    'Отметьте, где вы говорите «должен» про соцсети.',
    'Составьте правило: когда не отвечаете в комментариях.',
    'Один день — только личные сообщения, без публичных постов.',
    'Прочитайте 10 своих комментариев — тон совпадает с ценностями?',
    'Выберите аккаунт для вдохновения и разберите, что резонирует.',
    'Напишите «письмо себе» о цифровой усталости.',
    'Сделайте скриншот ленты — выделите, что вы выбрали сами.',
    'Практика «один экран времени» до открытия ленты.',
    'Составьте список триггеров, после которых вы постите импульсивно.',
    'Публикация благодарности без визуала — только текст.',
    'Ответьте: зачем вам лайки? одно честное предложение.',
    'Спросите: что вы боитесь потерять, если быть проще онлайн?',
    'Мини-ритуал: 3 вдоха перед публикацией.',
    'Выберите одну площадку для эксперимента с тоном.',
    'Запишите наблюдение о групповом давлении в чате.',
    'Составьте «конституцию приватности» из 3 пунктов.',
    'День без сравнения себя с чужими достижениями в ленте.'
  ];

  for (var a = 0; a < axes.length; a++) {
    for (var t = 0; t < 5; t++) {
      var base = templates[a * 5 + t] || templates[t];
      tasks.push({
        id: 'ch_' + axes[a] + '_' + t,
        axis: axes[a],
        axisLabel: labels[axes[a]],
        dayIndex: tasks.length,
        title: labels[axes[a]] + ': день ' + (t + 1),
        description: base
      });
    }
  }

  global.CLChallengesData = {
    programs: [
      {
        id: 'p7',
        name: '7 дней — базовая трансформация',
        days: 7,
        taskIds: tasks.slice(0, 7).map(function (x) {
          return x.id;
        })
      },
      {
        id: 'p30',
        name: '30 дней — полная программа',
        days: 30,
        taskIds: tasks.slice(0, 30).map(function (x) {
          return x.id;
        })
      }
    ],
    tasks: tasks
  };
})(typeof window !== 'undefined' ? window : this);
