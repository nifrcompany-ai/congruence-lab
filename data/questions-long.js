  function build(module, texts, offset) {
    var arr = [];
    for (var i = 0; i < texts.length; i++) {
      var controlPair = null;
      if (i === 12) controlPair = offset + 0;
      if (i === 13) controlPair = offset + 1;
      arr.push({
        id: 'L' + (offset + i),
        module: module,
        axisIndex: i % 6,
        text: texts[i],
        controlPair: controlPair,
        isControl: i >= 12
      });
    }
    return arr;
  }

  var questions = []
    .concat(build('real', realT, 0))
